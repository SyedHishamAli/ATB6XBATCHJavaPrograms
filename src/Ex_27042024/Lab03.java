package Ex_27042024;

public class Lab03 {
    public static void main(String[] args) {
        //For loop comes with all three(initial point; condition; inc/dec) aspects at once then execution.


        for(int j=1; j<=10; j++){
            for(int i=1; i<=10; i++){
                System.out.println(j+" X "+i+"= "+j*i);
            }
        }

    }
}

